const ARX_LANGUAGES = [
  { id: 'en', label: 'English',     native: 'English' },
  { id: 'zh', label: '华语',         native: '华语 · Chinese' },
  { id: 'vi', label: 'Tiếng Việt',  native: 'Tiếng Việt · Vietnamese' },
];

window.__ARX_LANG = window.__ARX_LANG || 'en';

function useT() {
  const [lang, setLang] = React.useState(window.__ARX_LANG || 'en');
  React.useEffect(() => {
    const h = () => setLang(window.__ARX_LANG || 'en');
    window.addEventListener('arx:lang-changed', h);
    return () => window.removeEventListener('arx:lang-changed', h);
  }, []);
  return React.useCallback((key, vars) => {
    const dict   = ARX_TRANSLATIONS[lang] || ARX_TRANSLATIONS.en;
    const fallEN = ARX_TRANSLATIONS.en;
    let s = dict[key] != null ? dict[key] : (fallEN[key] != null ? fallEN[key] : key);
    if (vars) for (const k in vars) s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
    return s;
  }, [lang]);
}

function useLangId() {
  const [lang, setLang] = React.useState(window.__ARX_LANG || 'en');
  React.useEffect(() => {
    const h = () => setLang(window.__ARX_LANG || 'en');
    window.addEventListener('arx:lang-changed', h);
    return () => window.removeEventListener('arx:lang-changed', h);
  }, []);
  return lang;
}

function useLangLabel() {
  const id = useLangId();
  const item = ARX_LANGUAGES.find(l => l.id === id);
  return item ? item.native : 'English';
}

Object.assign(window, { ARX_TRANSLATIONS, ARX_LANGUAGES, useT, useLangId, useLangLabel });
